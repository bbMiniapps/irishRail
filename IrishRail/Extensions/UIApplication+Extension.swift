import Foundation
import UIKit

extension UIApplication {
    
    var currentWindow: UIWindow? {
        connectedScenes
            .lazy
            .filter({$0.activationState == .foregroundActive})
            .compactMap({$0 as? UIWindowScene})
            .first?.windows
            .filter({$0.isKeyWindow}).first
    }
    
}
